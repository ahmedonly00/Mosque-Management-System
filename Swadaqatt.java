    private void editSwadaqatActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editSwadaqatActionPerformed
        // TODO add your handling code here:
        try{
            // make all field required
            if(id.getText().trim().isEmpty()){
                JOptionPane.showMessageDialog(this, "Swadaqat ID is required");
            }else{

                // Capture Data entered by user
                Swadaqat theFacility = new Swadaqat();
                int idii = Integer.parseInt(id.getText());
                theFacility.setSwadaqatId(idii);
                
                theFacility.setName(name.getText());
                theFacility.setDescription(description.getText());
                int idd = Integer.parseInt(amount.getText());
                theFacility.setAmount(idd);
                
                // calling DAO
                Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1", 5000);
                SwadaqatService service = (SwadaqatService)theRegistry.lookup("swa");
                Swadaqat dep = service.updateSwadaqat(theFacility);
                if(dep!=null){
                    JOptionPane.showMessageDialog(this, "Swadaqat Edited Successful");
                    addRowsDataToTable();
                    clearFields();
                }else{
                    JOptionPane.showMessageDialog(this, "Swadaqat Not Edited Successful");
                }
            }
        }catch(Exception ex){
            ex.printStackTrace();
        }
    }//GEN-LAST:event_editSwadaqatActionPerformed

