    private void addRowsDataToTable() throws RemoteException, NotBoundException{
        tableModel.setRowCount(0);
        Registry theRegistry = LocateRegistry.getRegistry("127.0.0.1", 5000);
        SwadaqatService service = (SwadaqatService)theRegistry.lookup("swa");
        List<Swadaqat> facilitiess = service.findAll();
        for(Swadaqat theFacilities : facilitiess){
            tableModel.addRow(new Object[]{
                theFacilities.getSwadaqatId(),
                theFacilities.getName(),
                theFacilities.getDescription(),
                theFacilities.getAmount(),
            });
        }
    }
    
